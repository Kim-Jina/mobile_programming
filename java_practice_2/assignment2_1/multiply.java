public class multiply {
    int mul(int n1, int n2){    // multiply function which has two integer parameters
        return n1*n2;   // return n1xn2
    }
    int mul(int n1, int n2, int n3){    // multiply function which has three integer parameters
        return n1*n2*n3;    // return n1xn2xn3
    }
    double mul(double n1, double n2){   // multiply function which has two double parameters
        return n1*n2;   // return n1xn2
    }
}
